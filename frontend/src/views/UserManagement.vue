<template>
  <div id="userManagementPage" class="content-container">
    <h1>用户管理</h1>
    <div class="handle-box">
      <el-input v-model="query.name" placeholder="用户名" class="handle-input mr10"></el-input>
      <el-button type="primary" :icon="Search" @click="handleSearch">搜索</el-button>
      <el-button type="primary" :icon="Plus" @click="openAddDialog">新增</el-button>
    </div>
    <el-table
      :data="tableData"
      border
      class="table"
      ref="multipleTable"
      header-cell-class-name="table-header"
    >
      <el-table-column
        prop="id"
        label="ID"
        width="55"
        align="center"
        v-if="false"
      ></el-table-column>
      <el-table-column type="expand">
        <template #default="props">
          <div m="4">
            <p m="t-0 b-2">角色: {{ props.row.roles }}</p>
            <p m="t-0 b-2">权限: {{ props.row.permissions }}</p>
            <p m="t-0 b-2">团队: {{ props.row.teamName }}</p>
            <p m="t-0 b-2">年龄: {{ props.row.age }}</p>
            <p m="t-0 b-2">地址: {{ props.row.address }}</p>
            <p m="t-0 b-2">性别: {{ props.row.gender }}</p>
            <p m="t-0 b-2">上次登录时间: {{ props.row.lastLoginTime }}</p>
            <p m="t-0 b-2">上次登录IP: {{ props.row.lastLoginIp }}</p>
            <p m="t-0 b-2">注册时间: {{ props.row.createTime }}</p>
            <p m="t-0 b-2">更新时间: {{ props.row.updateTime }}</p>
            <p m="t-0 b-2">状态: {{ props.row.status }}</p>
            <p m="t-0 b-2">是否启用: {{ props.row.isActive }}</p>
            <p m="t-0 b-2">是否锁定: {{ props.row.isLocked }}</p>
          </div>
        </template>
      </el-table-column>
      <el-table-column prop="username" label="用户名"></el-table-column>
      <el-table-column prop="name" label="昵称"></el-table-column>
      <el-table-column prop="phone" label="手机号"></el-table-column>
      <el-table-column prop="email" label="邮箱"></el-table-column>
      <el-table-column label="头像(查看大图)" align="center">
        <template #default="scope">
          <el-image
            class="table-td-avatar"
            :src="scope.row.avatar"
            :z-index="10"
            :preview-src-list="[scope.row.avatar]"
            preview-teleported
          >
          </el-image>
        </template>
      </el-table-column>
      <el-table-column prop="department" label="部门"></el-table-column>

      <el-table-column label="操作" width="220" align="center">
        <template #default="scope">
          <el-button
            text
            :icon="Edit"
            @click="openEditDialog(scope.$index, scope.row)"
            v-permission="15"
          >
            编辑
          </el-button>
          <el-button
            text
            :icon="Delete"
            class="red"
            @click="handleDelete(scope.$index)"
            v-permission="16"
          >
            删除
          </el-button>
        </template>
      </el-table-column>
    </el-table>
    <div class="pagination">
      <el-pagination
        background
        layout="total, prev, pager, next"
        :current-page="query.pageIndex"
        :page-size="query.pageSize"
        :total="pageTotal"
        @current-change="handlePageChange"
      ></el-pagination>
    </div>

    <!-- 新增和编辑弹窗 -->
    <UserDialog
      v-model:visible="dialogVisible"
      :is-edit="isEdit"
      :form-data="currentFormData"
      @save="handleSave"
    />
  </div>
</template>

<script lang="ts" setup>
import { ref, reactive, onMounted } from 'vue'
import { ElMessage, ElMessageBox, FormInstance, FormRules } from 'element-plus'
import { Delete, Edit, Search, Plus } from '@element-plus/icons-vue'
import { useUserStore } from '~/store/user'
import { getUserList, addUser, updateUser } from '~/api/user'
import { UserDTO } from '~/types/user'
import UserDialog from './UserDialog.vue'

const dialogVisible = ref(false)
const isEdit = ref(false)
const currentFormData = ref<UserDTO>({
  id: '',
  username: '',
  password: '',
  confirmPassword: '',
  name: '',
  phone: '',
  email: '',
  avatar: '',
  department: '',
  roles: [],
  permissions: [],
  status: 1,
  isActive: true,
  isLocked: false
})
const handleSave = async (formData: UserDTO) => {
  try {
    const res = isEdit.value ? await updateUser(formData) : await addUser(formData)

    if (res.code === 200) {
      ElMessage.success(isEdit.value ? '修改成功' : '新增成功')
      // 重新获取数据
      getData()
    } else {
      ElMessage.error(res.message)
    }
    dialogVisible.value = false
  } catch (error) {
    ElMessage.error(error instanceof Error ? error.message : '操作失败')
  }
}

const openAddDialog = () => {
  isEdit.value = false
  currentFormData.value = {
    id: '',
    username: '',
    password: '',
    confirmPassword: '',
    name: '',
    phone: '',
    email: '',
    avatar: '',
    department: '',
    roles: [],
    permissions: [],
    status: 1,
    isActive: true,
    isLocked: false
  }
  dialogVisible.value = true
}
const openEditDialog = (idx: number, row: UserDTO) => {
  isEdit.value = true
  currentFormData.value = { ...row }
  dialogVisible.value = true
}

// 表单定义
const addFormRef = ref<FormInstance>()
const editFormRef = ref<FormInstance>()

const query = reactive({
  name: '',
  pageIndex: 1,
  pageSize: 10
})
const tableData = ref<UserDTO[]>([])
const pageTotal = ref(0)
const userStore = useUserStore()
const addVisible = ref(false)
let addForm = reactive<UserDTO>({
  username: 'testuser1',
  password: 'password',
  confirmPassword: 'password',
  name: '测试昵称1',
  phone: '13911113333',
  email: 'test1@gmail.com',
  avatar: '',
  department: '',
  roles: [],
  permissions: [],
  status: 1,
  isActive: true,
  isLocked: false
})

const getMaxPageIndex = () => {
  if (pageTotal.value === 0) {
    return 1
  }

  const maxPageIndex = Math.ceil((pageTotal.value + 1) / query.pageSize)
  return maxPageIndex > 0 ? maxPageIndex : 1
}

const editVisible = ref(false)
let editForm = reactive<UserDTO>({
  id: '',
  username: '',
  name: '',
  phone: '',
  email: '',
  avatar: '',
  department: '',
  roles: [],
  permissions: [],
  status: 1,
  isActive: true,
  isLocked: false
})

const saveEdit = async () => {
  if (!editFormRef.value) return
  editVisible.value = false

  await editFormRef.value.validate(async (valid: boolean) => {
    if (valid) {
      try {
        const res = await updateUser(editForm)
        if (res.code === 200) {
          ElMessage.success('修改成功')
          getData()
        } else {
          ElMessage.error(res.message)
        }
      } catch (error) {
        ElMessage.error(error instanceof Error ? error.message : '修改失败')
      }
    } else {
      console.log('#### editFormRef.value', editFormRef.value)
      ElMessage.error('修改失败，验证未通过')
    }
  })
}

// 获取表格数据
const getData = async () => {
  try {
    const res = await getUserList({
      username: query.name,
      pageIndex: query.pageIndex,
      pageSize: query.pageSize
    })

    if (res.code === 200) {
      console.log('#### res.data.items', res.data.items)
      tableData.value = res.data.items
      pageTotal.value = res.data.total
    } else {
      console.log('#### res.code', res.code)
      console.log('#### res.message', res.message)
      console.log('#### res.data', res.data)
      ElMessage.error(res.message)
    }
  } catch (error) {
    ElMessage.error(error instanceof Error ? error.message : '获取数据失败')
  }
}

onMounted(() => {
  const pageIndex = localStorage.getItem('AMSCurentUserManagementPageIndex')
  if (pageIndex) {
    query.pageIndex = Number(pageIndex)
  }
  getData()
})

const handleClear = () => {
  query.name = ''
  query.pageIndex = 1
  getData()
}

// 搜索操作
const handleSearch = async () => {
  query.pageIndex = 1
  try {
    await getData()
  } catch (error) {
    ElMessage.error(error instanceof Error ? error.message : '获取数据失败')
  }
}

// 分页导航
const handlePageChange = async (val: number) => {
  query.pageIndex = val
  localStorage.setItem('AMSCurentUserManagementPageIndex', val.toString())

  try {
    await getData()
  } catch (error) {
    ElMessage.error(error instanceof Error ? error.message : '获取数据失败')
  }
}

// 删除操作
const handleDelete = (index: number) => {
  // 二次确认删除
  ElMessageBox.confirm('确定要删除吗？', '提示', {
    type: 'warning'
  })
    .then(() => {
      ElMessage.success('删除成功')
      tableData.value.splice(index, 1)
    })
    .catch(() => {})
}
</script>

